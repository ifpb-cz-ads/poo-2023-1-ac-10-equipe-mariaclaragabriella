public class ConversaoDeUnidadesDeArea {
    public static double metroParaPesQuadrados(double metro){
        return metro * 10.76;
    }

    public static double pesParaCentimetroQuadrado(double pes){
        return pes * 929;
    }

    public static double milhaParaAcres(double milha){
        return milha * 640;
    }

    public static double acreParaPesQuadrados(double acre){
        return acre * 43560;
    }
}
